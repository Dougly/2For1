import UIKit

extension UIColor {
    
    static var themeBlue: UIColor {
        return UIColor(colorLiteralRed: 186/255, green: 247/255, blue: 255/255, alpha: 1)
    }
    
    static var themeGreen: UIColor {
        return UIColor(colorLiteralRed: 39/255, green: 85/255, blue: 147/255, alpha: 1)
    }
    
    static var themeOrange: UIColor {
        return UIColor(colorLiteralRed: 255/255, green: 203/255, blue: 83/255, alpha: 1)
    }
    
    static var themeDarkGray: UIColor {
        return UIColor(colorLiteralRed: 36/255, green: 36/255, blue: 36/255, alpha: 1)
    }

    static var themeDarkGreen: UIColor {
        return UIColor(colorLiteralRed: 28/255, green: 216/255, blue: 143/255, alpha: 1)
    }

}
